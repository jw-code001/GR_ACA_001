function createSurveySheet() {
  // 1. 새 스프레드시트 생성
  var ss = SpreadsheetApp.create('피부 고민 설문조사 시스템 (Streamlit 연동용)');

  // 2. 첫 번째 시트 '질문관리' 탭 세팅
  var sheet1 = ss.getSheets()[0];
  sheet1.setName('질문관리');

  // 질문관리 탭 헤더 설정
  var headers1 = ['문항번호', '질문내용', '질문유형', '선택지'];
  sheet1.appendRow(headers1);

  // 12개 문항 데이터 자동 입력 (스트림릿 렌더링용)
  var questions = [
    [1, '귀하의 연령대는 어떻게 되십니까?', 'radio', '10대, 20대, 30대, 40대, 50대 이상'],
    [2, '귀하의 성별은 무엇입니까?', 'radio', '여성, 남성, 선택하지 않음'],
    [3, '현재 가장 신경 쓰이는 피부 고민은 무엇입니까? (다중 선택)', 'checkbox', '여드름 및 트러블, 모공 및 피지, 건조함 및 속당김, 주름 및 탄력 저하, 색소 침착 (기미, 잡티 등), 피부 톤 및 결 개선'],
    [4, '평소 피부과나 에스테틱(피부관리실)을 얼마나 자주 방문하시나요?', 'radio', '전혀 방문하지 않음 (홈케어만 진행), 1년에 1~2회 (특별한 일이 있을 때만), 2~3개월에 1회, 한 달에 1회, 한 달에 2회 이상'],
    [5, '에스테틱 방문 대신 홈케어를 선호하거나 병행하시는 주된 이유는 무엇인가요?', 'radio', '비용 부담이 적어서, 샵에 방문할 시간을 내기 어려워서, 홈케어 기기/제품만으로도 충분히 만족해서, 샵의 과도한 시술 권유나 영업이 부담스러워서, 내 피부 상태에 맞춰 스스로 관리하는 것이 편해서, 해당 없음 (에스테틱만 이용)'],
    [6, '홈케어를 위해 주로 사용하시거나 알고 계신 스킨케어/뷰티 디바이스 브랜드가 있다면 적어주세요.', 'text', ''],
    [7, '에스테틱 관리를 받는다면, 가장 집중적으로 관리받고 싶은 부위는 어디인가요?', 'checkbox', '얼굴 전체 (윤곽, 탄력 등), 국소 부위 (눈가, 팔자주름 등), 목 (목주름, 탄력 등), 바디 트러블 (등, 가슴 등), 두피 및 헤어라인'],
    [8, '효과가 확실하다면, 한 달에 몇 회 정도 에스테틱을 방문하는 것이 가장 이상적이라고 생각하시나요?', 'radio', '한 달에 1회, 한 달에 2회 (격주), 한 달에 4회 (매주), 기타'],
    [9, '피부 관리를 위해 "한 달에" 지출할 수 있는 최대 비용은 얼마인가요?', 'radio', '5만 원 미만, 5만 원 ~ 10만 원 미만, 10만 원 ~ 20만 원 미만, 20만 원 ~ 30만 원 미만, 30만 원 이상'],
    [10, '1회 관리(약 60분~90분 소요)를 기준으로, 적절하다고 생각하는 1회당 비용은 얼마인가요?', 'radio', '3만 원 미만, 3만 원 ~ 5만 원 미만, 5만 원 ~ 8만 원 미만, 8만 원 ~ 12만 원 미만, 12만 원 이상'],
    [11, '피부 관리실(에스테틱)을 선택할 때 가장 중요하게 생각하는 요소는 무엇입니까?', 'radio', '합리적인 가격 및 프로모션, 집이나 직장과의 거리 (접근성), 실제 고객의 후기 및 지인 추천, 관리사의 전문성 및 보유 기기, 1:1 맞춤 상담 및 프라이빗한 시설'],
    [12, '그 외에 에스테틱 서비스에 바라는 점이나, 평소 피부 관리에 있어 어려운 점이 있다면 자유롭게 적어주세요.', 'text', '']
  ];

  // 한 번에 시트에 뿌려주기
  sheet1.getRange(2, 1, questions.length, 4).setValues(questions);
  sheet1.getRange('A1:D1').setFontWeight('bold'); // 헤더 굵게 처리

  // 3. 두 번째 시트 '응답결과' 탭 세팅
  var sheet2 = ss.insertSheet('응답결과');
  var headers2 = ['식별자(연락처/이메일)'];
  
  // 질문 1번부터 12번까지 헤더 자동 생성
  for (var i = 0; i < questions.length; i++) {
    headers2.push(questions[i][0] + '번 응답');
  }
  
  sheet2.appendRow(headers2);
  sheet2.getRange('A1:M1').setFontWeight('bold');

  // 4. 실행 로그에 완성된 구글 시트 링크 띄우기
  Logger.log('✅ 1초만에 완성된 구글 시트 링크: ' + ss.getUrl());
}
